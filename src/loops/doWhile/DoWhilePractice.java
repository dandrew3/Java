package loops.doWhile;

public class DoWhilePractice {
    public static void main(String[] args) {
        // show your age from 21 to 26
        int age =21;
        do {
            System.out.println("you age is: "+age);
            age++;
        }while (age < 15);
        age =21;
        do {
            System.out.println("you age is: "+age);
            age++;
        }while (age < 27);
    }
}
